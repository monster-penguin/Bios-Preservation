# File-open checks
# ---------------------------------------------------------------------------

def _file_is_locked(path: str) -> bool:
    """Return True if *path* exists and cannot be opened exclusively for writing."""
    if not os.path.exists(path):
        return False
    try:
        with open(path, "a+b"):
            return False
    except OSError:
        return True


def _files_to_check(step: str, config: configparser.ConfigParser, base_dir: str) -> list[str]:
    """Return the list of file paths to check for locks before running *step*."""
    def r(rel: str) -> str:
        p = Path(rel)
        return str(p if p.is_absolute() else Path(base_dir) / p)

    if step == "update":
        out = config.get("update", "output_dir", fallback="update")
        return [
            r(f"{out}/combined_platform_manifest.json"),
            r(f"{out}/combined_platform_manifest.csv"),
        ]

    if step == "build":
        sqlar = config.get("build", "sqlar_output",  fallback="build/bios_database.sqlar")
        jout  = config.get("build", "json_output",   fallback="build/combined_platform_build.json")
        cout  = config.get("build", "csv_output",    fallback="build/combined_platform_build.csv")
        return [r(sqlar), r(jout), r(cout)]

    if step == "stage":
        sqlar = config.get("stage", "sqlar_input",    fallback="build/bios_database.sqlar")
        mf    = config.get("stage", "manifest_input", fallback="build/combined_platform_build.json")
        return [r(sqlar), r(mf)]

    if step == "report":
        sqlar = config.get("report", "sqlar_input",    fallback="build/bios_database.sqlar")
        mf    = config.get("report", "manifest_input", fallback="build/combined_platform_build.json")
        rdir  = config.get("report", "report_dir",     fallback="report")
        platforms = [
            "retrodeck", "retropie", "batocera", "emudeck",
            "recalbox",  "retrobat", "lakka",    "retroarch",
        ]
        enabled_csvs = [
            r(f"{rdir}/{p}_report.csv")
            for p in platforms
            if config.get("report", p, fallback="yes").strip().lower() in ("yes", "true", "1")
        ]
        return [r(sqlar), r(mf)] + enabled_csvs

    if step in ("backup", "dump"):
        sqlar = config.get("build", "sqlar_output", fallback="build/bios_database.sqlar")
        return [r(sqlar)]

    # configure has no file-lock check
    return []


def _check_files_open(step: str, config: configparser.ConfigParser, base_dir: str) -> bool:
    """
    Check whether any files relevant to *step* are currently locked.
    Returns True if all clear, False if any file is locked.
    """
    locked = [f for f in _files_to_check(step, config, base_dir) if _file_is_locked(f)]
    if locked:
        print(f"\n  ERROR: the following file(s) are open in another application.")
        print(f"  Please close them before running {step.upper()}:\n")
        for f in locked:
            print(f"    {f}")
        print()
        return False
    return True


# ---------------------------------------------------------------------------
# Step runner
# ---------------------------------------------------------------------------

def _run_step(step: str, config: configparser.ConfigParser, base_dir: str) -> bool:
    """Import and run the named step.  Returns True on success."""
    print(f"\n{'='*60}")
    print(f"  STEP: {step.upper()}")
    print(f"{'='*60}")
    try:
        if step == "update":
            import bios_update
            return bios_update.run(config, base_dir)
        elif step == "build":
            import bios_build
            return bios_build.run(config, base_dir)
        elif step == "stage":
            import bios_stage
            return bios_stage.run(config, base_dir)
        elif step == "report":
            import bios_report
            return bios_report.run(config, base_dir)
        elif step == "backup":
            import bios_backup
            return bios_backup.run(config, base_dir)
        elif step == "dump":
            import bios_dump
            return bios_dump.run(config, base_dir)
        elif step == "configure":
            import bios_configure
            return bios_configure.run(config, base_dir)
        else:
            print(f"ERROR: unknown step {step!r}")
            return False
    except Exception:
        print(f"\nERROR: unhandled exception in step {step!r}:")
        traceback.print_exc()
        return False


def _print_summary(results: dict[str, bool], steps: list[str]) -> None:
    print(f"\n{'='*60}")
    print("  SUMMARY")
    print(f"{'='*60}")
    for step in steps:
        if step in results:
            mark = "OK  ✓" if results[step] else "FAILED ✗"
            print(f"  {step:12s}  {mark}")
        else:
            print(f"  {step:12s}  skipped")
    print()


