def _run_interactive(base_dir: Path, scripts_dir: Path) -> None:
    scripts_str = str(scripts_dir)
    if scripts_str not in sys.path:
        sys.path.insert(0, scripts_str)

    config = _load_config(base_dir)

    while True:
        print(f"\n{'='*60}")
        print("  BIOS Preservation Tool")
        print(f"{'='*60}")
        for i, (label, _) in enumerate(MENU_ITEMS, start=1):
            print(f"  {i}. {label}")
        print(f"  0. Exit")
        print(f"{'─'*60}")

        raw = input("  Enter number: ").strip()

        if raw == "0":
            print("  Goodbye.")
            sys.exit(0)

        if not raw.isdigit() or not (1 <= int(raw) <= len(MENU_ITEMS)):
            print(f"  Invalid selection. Please enter a number between 0 and {len(MENU_ITEMS)}.")
            continue

        choice      = int(raw)
        label, step = MENU_ITEMS[choice - 1]

        # File-open check (configure has none)
        if not _check_files_open(step, config, str(base_dir)):
            continue

        # Run the step and show summary, then loop back to menu
        success = _run_step(step, config, str(base_dir))

        # After configure completes, reload config in case user.conf was
        # created, updated, or deleted
        if step == "configure":
            config = _load_config(base_dir)

        _print_summary({step: success}, [step])


