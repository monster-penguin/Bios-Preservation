# CLI (arguments provided)
# ---------------------------------------------------------------------------

def _run_cli(base_dir: Path, scripts_dir: Path) -> None:
    parser = argparse.ArgumentParser(
        prog="bios_preservation.py",
        description="BIOS Preservation — master launcher",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Steps:
  update     Parse platform YAMLs -> update/combined_platform_manifest.*
  build      Scan sources, build database -> build/bios_database.sqlar + build/combined_platform_build.*
  stage      Stage files per platform -> stage/<platform>/
  report     Generate per-platform status reports -> report/<platform>_report.csv

Examples:
  python bios_preservation.py update
  python bios_preservation.py build
  python bios_preservation.py stage
  python bios_preservation.py update build stage
  python bios_preservation.py update build stage --continue-on-error
        """,
    )
    parser.add_argument(
        "steps",
        nargs="+",
        choices=VALID_STEPS,
        metavar="STEP",
        help=f"One or more steps: {', '.join(VALID_STEPS)}",
    )
    parser.add_argument(
        "--continue-on-error",
        action="store_true",
        default=False,
        help="Continue to the next step even if a step fails",
    )

    args = parser.parse_args()

    # Deduplicate while preserving order
    seen:  set[str]  = set()
    steps: list[str] = []
    for s in args.steps:
        if s not in seen:
            steps.append(s)
            seen.add(s)

    scripts_str = str(scripts_dir)
    if scripts_str not in sys.path:
        sys.path.insert(0, scripts_str)

    config = _load_config(base_dir)

    results:    dict[str, bool] = {}
    overall_ok: bool = True

    for step in steps:
        if not _check_files_open(step, config, str(base_dir)):
            results[step] = False
            overall_ok = False
            if not args.continue_on_error:
                print(f"[launcher] Step {step!r} aborted (file locked) — stopping.")
                print("[launcher] Use --continue-on-error to run subsequent steps anyway.")
                break
            else:
                print(f"[launcher] Step {step!r} aborted (file locked) — continuing (--continue-on-error).")
            continue

        success = _run_step(step, config, str(base_dir))
        results[step] = success
        if not success:
            overall_ok = False
            if not args.continue_on_error:
                print(f"\n[launcher] Step {step!r} FAILED — stopping.")
                print("[launcher] Use --continue-on-error to run subsequent steps anyway.")
                break
            else:
                print(f"\n[launcher] Step {step!r} FAILED — continuing (--continue-on-error).")

    _print_summary(results, steps)
    sys.exit(0 if overall_ok else 1)


