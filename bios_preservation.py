def _load_config(base_dir: Path) -> configparser.ConfigParser:
    conf_path = base_dir / "configure" / "bios_preservation.conf"
    if not conf_path.exists():
        print(f"ERROR: configuration file not found: {conf_path}")
        print("Expected location: <bios_preservation>/configure/bios_preservation.conf")
        sys.exit(1)
    cfg = configparser.ConfigParser()
    # Load default conf first, then overlay user conf if present
    paths = [str(conf_path)]
    user_conf = base_dir / "configure" / "bios_preservation_user.conf"
    if user_conf.exists():
        paths.append(str(user_conf))
        print(f"[launcher] Using user configuration: {user_conf}")
    cfg.read(paths, encoding="utf-8")
    return cfg


