def _apply_unrar_tool(base_dir: Path) -> None:
    """
    Set rarfile.UNRAR_TOOL to the right binary, in priority order:
      1. Path saved in bios_preservation_user.conf  (user already resolved it)
      2. Well-known default install locations (WinRAR on Windows)
      3. Leave as-is (rarfile's own default, usually just 'unrar' on PATH)
    """
    try:
        import rarfile

        # ── 1. User-saved path from conf ──────────────────────────────────
        user_conf = base_dir / "configure" / "bios_preservation_user.conf"
        if user_conf.exists():
            cfg = configparser.ConfigParser()
            cfg.read(str(user_conf), encoding="utf-8")
            tool = cfg.get("build", "unrar_tool", fallback="").strip()
            if tool and Path(tool).exists():
                rarfile.UNRAR_TOOL = tool
                return

        # ── 2. Well-known default locations ───────────────────────────────
        FALLBACK_PATHS = [
            r"C:\Program Files\WinRAR\UnRAR.exe",
            r"C:\Program Files (x86)\WinRAR\UnRAR.exe",
        ]
        for candidate in FALLBACK_PATHS:
            if Path(candidate).exists():
                rarfile.UNRAR_TOOL = candidate
                return

        # ── 3. Leave rarfile's default in place ───────────────────────────

    except Exception:
        pass


def main() -> None:
    base_dir    = Path(__file__).resolve().parent
    scripts_dir = base_dir / "scripts"

    if not scripts_dir.is_dir():
        print(f"ERROR: scripts directory not found: {scripts_dir}")
        print("Expected: <bios_preservation>/scripts/")
        sys.exit(1)

    # Apply saved/default unrar_tool path before dependency check runs
    _apply_unrar_tool(base_dir)

    _check_dependencies(base_dir)

    if sys.argv[1:]:
        _run_cli(base_dir, scripts_dir)
    else:
        _run_interactive(base_dir, scripts_dir)


if __name__ == "__main__":
    main()
