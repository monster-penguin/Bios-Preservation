def _persist_unrar_tool(path: str, base_dir: Path) -> None:
    """Save the unrar tool path to bios_preservation_user.conf so it persists across runs."""
    import configparser
    user_conf = base_dir / "configure" / "bios_preservation_user.conf"
    cfg = configparser.ConfigParser()
    if user_conf.exists():
        cfg.read(str(user_conf), encoding="utf-8")
    if not cfg.has_section("build"):
        cfg.add_section("build")
    cfg.set("build", "unrar_tool", path)
    user_conf.parent.mkdir(parents=True, exist_ok=True)
    with open(user_conf, "w", encoding="utf-8") as fh:
        fh.write("# bios_preservation_user.conf\n")
        fh.write("# Generated by bios_configure.py / bios_build.py\n")
        fh.write("# Delete this file to restore defaults.\n\n")
        cfg.write(fh)
    print(f"  Saved to: {user_conf}")


def _check_dependencies(base_dir: Path) -> None:
    """
    Check for required Python packages and the UnRAR binary.
    Offer to install missing Python packages via pip.
    Prints a warning (but does not abort) if anything is missing.
    """
    import importlib
    import subprocess

    PYTHON_DEPS = [
        ("yaml",   "pyyaml",  "PyYAML  — YAML parsing (required for Step 1: Update)"),
        ("py7zr",  "py7zr",   "py7zr   — 7z archive support (required for .7z sources)"),
        ("rarfile","rarfile",  "rarfile — RAR archive support (required for .rar sources)"),
    ]

    missing_packages: list[tuple[str, str, str]] = []
    for import_name, pip_name, description in PYTHON_DEPS:
        try:
            importlib.import_module(import_name)
        except ImportError:
            missing_packages.append((import_name, pip_name, description))

    # Only check for the unrar binary if rarfile itself is installed —
    # let rarfile do its own detection since it knows all the right paths.
    missing_unrar = False
    unrar_tool_path = "unrar"
    if "rarfile" not in [p[0] for p in missing_packages]:
        try:
            import rarfile
            unrar_tool_path = getattr(rarfile, "UNRAR_TOOL", "unrar")
            try:
                subprocess.run(
                    [unrar_tool_path],
                    stdout=subprocess.DEVNULL,
                    stderr=subprocess.DEVNULL,
                )
            except (FileNotFoundError, OSError):
                missing_unrar = True
        except Exception:
            missing_unrar = True

    if not missing_packages and not missing_unrar:
        return  # everything present — silent

    print(f"\n{'='*60}")
    print("  DEPENDENCY CHECK")
    print("─" * 60)

    if missing_packages:
        print("\n  Missing Python packages:\n")
        for _, pip_name, description in missing_packages:
            print(f"    • {description}")

    if missing_unrar:
        print("\n  Missing binary:\n")
        print(f"    • UnRAR — required for .rar archive support")
        print(f"      rarfile is looking for: {unrar_tool_path!r}")
        print(f"      If WinRAR is installed, enter its UnRAR.exe path below to fix this.")
        print(f"      Common location: C:\\Program Files\\WinRAR\\UnRAR.exe")
        print(f"      To install: winget install RARLab.WinRAR  (Windows)")
        print(f"                  brew install rar              (macOS)")
        print(f"                  sudo apt install unrar        (Linux)")
        print()
        raw = input("  Enter path to UnRAR.exe (or Enter to skip): ").strip().strip('"').strip("'")
        if raw:
            # Verify the path actually works
            try:
                subprocess.run(
                    [raw],
                    stdout=subprocess.DEVNULL,
                    stderr=subprocess.DEVNULL,
                )
                # If we get here without FileNotFoundError, it works — save it
                import rarfile as _rf
                _rf.UNRAR_TOOL = raw
                # Also persist to user conf so it survives restarts
                _persist_unrar_tool(raw, base_dir)
                print(f"  UnRAR path set to: {raw!r}")
                missing_unrar = False
            except (FileNotFoundError, OSError):
                print(f"  Could not launch {raw!r} — path not saved.")

    if missing_packages:
        print()
        raw = input("  Install missing Python packages now via pip? [Y/N]: ").strip().lower()
        if raw in ("y", ""):
            import subprocess
            pip_names = [p for _, p, _ in missing_packages]
            cmd = [sys.executable, "-m", "pip", "install"] + pip_names
            print(f"\n  Running: {' '.join(cmd)}\n")
            result = subprocess.run(cmd)
            if result.returncode == 0:
                print("\n  Packages installed successfully.")
            else:
                print("\n  pip install failed — please install manually.")
        else:
            print("  Skipping — some features may not work.")

    print()


