"""
bios_preservation.py — Master launcher

Lives at the root of the bios_preservation/ folder.
Finds configuration in  ./configure/bios_preservation.conf
Uses user overrides from ./configure/bios_preservation_user.conf  (if present)
Imports scripts from    ./scripts/

Interactive usage (no arguments):
  python bios_preservation.py

Command-line usage:
  python bios_preservation.py update
  python bios_preservation.py build
  python bios_preservation.py stage
  python bios_preservation.py update build stage
  python bios_preservation.py update build stage --continue-on-error
"""

from __future__ import annotations

import argparse
import configparser
import os
import sys
import traceback
from pathlib import Path

VALID_STEPS = ("update", "build", "stage", "report")

# Menu entries: (display label, step key)
MENU_ITEMS = [
    ("Update",    "update"),
    ("Build",     "build"),
    ("Stage",     "stage"),
    ("Report",    "report"),
    ("Backup",    "backup"),
    ("Dump",      "dump"),
    ("Configure", "configure"),
]


